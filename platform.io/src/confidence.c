#define CONFIDENCE_INTERVALS_SIZE (sizeof(confidenceSampleNums) / sizeof(uint16_t))

void getConfidenceIntervals(uint32_t sampleNum,
                            uint32_t *lowerConfidenceInterval,
                            uint32_t *upperConfidenceInterval)
{
    uint32_t left = 0;
    uint32_t right = CONFIDENCE_INTERVALS_SIZE - 1;
    int32_t index = -1;

    while (left <= right)
    {
        uint32_t mid = (left + right) / 2;
        uint32_t confidenceSampleNum = confidenceSampleNums[mid];

        if (confidenceSampleNum < sampleNum)
            left = mid + 1;
        else if (confidenceSampleNum > sampleNum)
            right = mid - 1;
        else
        {
            index = mid;

            break;
        }
    }

    if (index == -1)
        index = right;

    *lowerConfidenceInterval = confidenceLowerIntervals[index];
    *upperConfidenceInterval = confidenceUpperIntervals[index];
}
